int main(void)
{
  setup();
  led_off();

  PwmGenerator gen = {
    .gpio_port = GPIOB,
    .gpios = GPIO1,
    .default_cycle = .5,
  };
  pwm_init(&gen);

  printf("Note 500\n");
  // pwm_gen_rt(&gen, 500, 200, .1);
  // pwm_gen_rt(&gen, 500, 200, .2);
  // pwm_gen_rt(&gen, 500, 200, .3);
  // pwm_gen_rt(&gen, 500, 200, .4);
  pwm_gen_rt(&gen, 20, 1000, .5);
  printf("Done\n");
  while (true)
  {
  }
}

/**
 * RTC 计时器中断
 *
 * 每秒执行一次
 */
void rtc_isr(void)
{
  rtc_clear_flag(RTC_SEC);
}

